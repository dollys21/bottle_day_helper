package com.bdh.app.model;

public class BottleConfiguration {

	private int bottleId;
	private String bottleDescription;
	private int bottleValue;
	private int bottleSize;
	private int bottleAddedBy;
	
	public int getBottleId() {
		return bottleId;
	}
	public void setBottleId(int bottleId) {
		this.bottleId = bottleId;
	}
	public String getBottleDescription() {
		return bottleDescription;
	}
	public void setBottleDescription(String bottleDescription) {
		this.bottleDescription = bottleDescription;
	}
	public int getBottleAddedBy() {
		return bottleAddedBy;
	}
	public void setBottleAddedBy(int bottleAddedBy) {
		this.bottleAddedBy = bottleAddedBy;
	}
	public int getBottleValue() {
		return bottleValue;
	}
	public void setBottleValue(int bottleValue) {
		this.bottleValue = bottleValue;
	}
	public int getBottleSize() {
		return bottleSize;
	}
	public void setBottleSize(int bottleSize) {
		this.bottleSize = bottleSize;
	}
	
	
	
}
